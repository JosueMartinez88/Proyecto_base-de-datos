    /**
     * Muestra los resultados de una consulta en un JTable dentro de un JOptionPane.
     * Si no hay resultados o ocurre un error, muestra mensajes apropiados.
     * 
     * @param rs ResultSet con los datos de la consulta
     */
    private void mostrarResultados(ResultSet rs) {
        if (rs == null) {
            JOptionPane.showMessageDialog(this, "No hay resultados.", "Sin resultados", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        try {
            ResultSetMetaData metaData = rs.getMetaData();
            int columnas = metaData.getColumnCount();

            DefaultTableModel modelo = new DefaultTableModel();
            // Agrega las columnas al modelo
            for (int i = 1; i <= columnas; i++) {
                modelo.addColumn(metaData.getColumnName(i));
            }
            int filas = 0;
            // Llena el modelo con las filas del ResultSet
            while (rs.next()) {
                Object[] fila = new Object[columnas];
                for (int i = 1; i <= columnas; i++) {
                    fila[i - 1] = rs.getObject(i);
                }
                modelo.addRow(fila);
                filas++;
            }
            rs.close();

            if (filas == 0) {
                JOptionPane.showMessageDialog(this, "No se encontraron alumnos.", "Sin resultados", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JTable tabla = new JTable(modelo);
                JScrollPane scroll = new JScrollPane(tabla);
                scroll.setPreferredSize(new java.awt.Dimension(500, 200));
                JOptionPane.showMessageDialog(this, scroll, "Resultados", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error al mostrar resultados: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }


