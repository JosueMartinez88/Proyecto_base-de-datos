    /**
     * Este método se llama al hacer click en el botón para buscar por ID.
     * Obtiene el ID ingresado, valida que sea un número y realiza la consulta.
     * 
     * @param evt Evento de mouse click
     */
    private void sendBuscarIdMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_sendBuscarIdMouseClicked
         if (tablaAlumnos == null) return;
        String texto = formattfieldId.getText().trim();
        if (texto.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor ingrese el id a buscar.");
            return;
        }
        try {
            int id = Integer.parseInt(texto);
            ResultSet rs = operacionesBD.buscarAlumnoPorId(tablaAlumnos, id);
            mostrarResultados(rs);
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Ingrese un número válido para el id.");
        }
    }//GEN-LAST:event_sendBuscarIdMouseClicked

